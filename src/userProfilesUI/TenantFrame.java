    private void btnChangePasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangePasswordActionPerformed
        if (!utils.FormChecker.checkEmptyTextFields(txtOldPassword, txtNewPassword, txtConfirmNewPassword)) {
            utils.ShowMessage.showMessageDialog(this, "Please enter empty fields");
            return;
        }
        if (!utils.FormChecker.checkPasswordField(txtOldPassword, txtNewPassword, txtConfirmNewPassword)) {
            utils.ShowMessage.showMessageDialog(this, "Password cannot less than 6 character");
            return;
        }
        try {
            if (!utils.FormChecker.checkMatches(txtNewPassword, txtConfirmNewPassword)) {
                utils.ShowMessage.showMessageDialog(this, "Password not match !");
                return;
            }
        } catch (NoSuchAlgorithmException ex) {
            this.taTenantLog.append("\n" + ex.getMessage());
        }
        //New tenant for new password
        Tenant ten = new Tenant(curr);
        tenant.setPassword(utils.Encrypt.enPass(String.valueOf(this.txtOldPassword.getPassword())));
        try {
            boolean hasExist = th.checkPassword(tenant);
            if (hasExist) {
                ten.setPassword(String.valueOf(this.txtNewPassword.getPassword()));
                boolean result = th.changeTenantPassword(ten);
                if (result) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Change Password successful!");
                    this.dispose();
                    LoginFrame login = new LoginFrame();
                    login.setVisible(true);
                }
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Please check your old password");
            }
        } catch (ClassNotFoundException ex) {
            this.taTenantLog.append("\n" + ex.getMessage());
        } catch (SQLException ex) {
            this.taTenantLog.append("\n" + ex.getMessage());
        }
    }//GEN-LAST:event_btnChangePasswordActionPerformed

