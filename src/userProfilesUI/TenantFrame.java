    private void setTenantFormValue() throws ClassNotFoundException, SQLException {
        if (curr.equals("")) {
            return;
        }
        tenant = th.getTenantById(curr);
        this.txtFirstname.setText(tenant.getFirstname());
        this.txtLastname.setText(tenant.getLastname());
        this.txtPhone.setText(tenant.getPhone());
        this.txtEmail.setText(tenant.getEmail());
        this.txtCurrentAddress.setText(tenant.getCurrentAddress());
        this.txtCityStateZip.setText(tenant.getCityStateZip());
    }

